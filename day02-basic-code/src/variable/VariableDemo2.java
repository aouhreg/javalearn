package variable;

public class VariableDemo2 {
    public static void main(String[] args) {
        //目標記住八種基本數據類型
        byte age = 12;
        short number = 32424;
        int money =23423423;
        //注意，隨便寫的證書阿玲是int類型，沒有超過ong範圍，但是超過int範圍就報錯，後面要加L跟l
        long lg =42342342L;

        float ft = 3.14F;
        double d = 3.14;

        char c = 'c';
        boolean b = true;
        //字符串引用數據烈性
        String s = "1213";

    }
}
