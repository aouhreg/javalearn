package operator;

public class OperatorDemo1 {
    public static void main(String[] args) {
        int a = 1;
        int b = 2;
        System.out.println(a + b);
        System.out.println(a - b);
        System.out.println(a * b);
        System.out.println(a / b);
        System.out.println(a % b);

        int c = 5;
        System.out.println("c == " + c);
        System.out.println(c + 5);
        System.out.println("c == " + c + 'a');
    }
}
